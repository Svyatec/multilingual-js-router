  const SUPPORTED = ['ru', 'en', 'de', 'fr', 'zh', 'ja', 'ko', 'th'];

  function detectLang() {
    /* статическая языковая страница (/de/ и т.п.): язык зашит в HTML и не переопределяется */
    const st = document.documentElement.dataset.staticLang;
    if (st && SUPPORTED.includes(st)) return st;
    const url = new URLSearchParams(location.search).get('lang');
    if (url && SUPPORTED.includes(url)) return url;
    const saved = localStorage.getItem('sk_lang');
    if (saved && SUPPORTED.includes(saved)) return saved;
    const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
    let cand = SUPPORTED.includes(nav) ? nav : 'en';
    /* на .com русский недопустим как язык по умолчанию — только по явному выбору
       (а выбор ru на .com уводит на .ru через set()); иначе международный сайт
       не должен показывать русский случайному ru-браузеру до баннера */
    if (INTL.isCom && cand === 'ru') cand = 'en';
    return cand;
  }

  const i18n = {
    lang: detectLang(),
    t(key) {
      return (D[this.lang] && D[this.lang][key]) || D.ru[key] || key;
    },
    set(lang) {
      if (!SUPPORTED.includes(lang)) return;
      /* региональная маршрутизация: каждый язык живёт по своему адресу —
         ru на .ru, en в корне .com, остальные в папках .com; переключение = навигация */
      if (INTL.LIVE && !INTL.isBot) {
        const cur = document.documentElement.dataset.staticLang || (INTL.isCom ? 'en' : 'ru');
        if (!INTL.isCom && lang !== 'ru') { location.href = INTL.target(lang); return; }
        if (INTL.isCom && lang !== cur) { location.href = INTL.target(lang); return; }
      }
      this.lang = lang;
      localStorage.setItem('sk_lang', lang);
      this.apply();
      document.dispatchEvent(new CustomEvent('langchange', { detail: lang }));
    },
    apply() {
      const lang = this.lang;
      document.documentElement.lang = lang;
      /* если ключа нет в словаре (например, устаревший кэш i18n.js при свежем
         HTML) — оставляем текст, зашитый в разметке, а не сырой ключ */
      const tr = k => { const v = this.t(k); return v === k ? null : v; };
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const v = tr(el.dataset.i18n);
        if (v !== null) el.textContent = v;
      });
      document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const v = tr(el.dataset.i18nHtml);
        if (v !== null) el.innerHTML = v;
      });
      document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const v = tr(el.dataset.i18nPh);
        if (v !== null) el.placeholder = v;
      });
      document.querySelectorAll('[data-i18n-tip]').forEach(el => {
        const v = tr(el.dataset.i18nTip);
        if (v !== null) el.dataset.tip = v;
      });
      document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const v = tr(el.dataset.i18nAria);
        if (v !== null) el.setAttribute('aria-label', v);
      });
      document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        const v = tr(el.dataset.i18nAlt);
        if (v !== null) el.alt = v;
      });
      document.querySelectorAll('[data-i18n-decode]').forEach(el => {
        const txt = tr(el.dataset.i18nDecode);
        if (txt !== null) { el.dataset.text = txt; el.textContent = txt; }
      });
      /* статические страницы (data-static-lang) несут СВОЙ локальный title/desc
         (например портфолио) — перетирать их домашними нельзя */
      if (!document.documentElement.dataset.staticLang) {
        if (D[lang]['page.title']) document.title = D[lang]['page.title'];
        const md = document.querySelector('meta[name="description"]');
        if (md && D[lang]['page.desc']) md.content = D[lang]['page.desc'];
      }
      const cur = document.getElementById('lang-cur');
      if (cur) cur.textContent = lang === 'zh' ? '中文' : lang === 'ja' ? '日本' : lang === 'ko' ? '한국' : lang === 'th' ? 'ไทย' : lang.toUpperCase();
    },
  };

  window.i18n = i18n;

